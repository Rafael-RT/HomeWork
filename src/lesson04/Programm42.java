package lesson04;

public class Programm42 {
    public static void main(String[] args) {
        int a = 10;
        a += 3;
        System.out.println("1 operation: " + a);

        a -= 7;
        System.out.println("2 operation: " + a);

        a *= 4;
        System.out.println("3 operation: " + a);

        a /= 8;
        System.out.println("4 operation: " + a);

        a %= 2;
        System.out.println("5 operation: " + a);

    }
}
